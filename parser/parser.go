// peekToken = ASSIGN
// let foo = 5;
//         ^ ^
// curToken = ASSIGN
// peekToken = INT
// let foo = 5;
//           ^^
// curToken = INT
// peekToken = SEMICOLON
// let foo = 5;
//            ^^
